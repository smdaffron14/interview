package com.deloitte.interview.solution.simple;

/**
 * This class represents a floor as unsed
 * in a simple solution to the Elevator Conundrum
 * CDT interview problem
 * 
 * @author MD
 */
public class Floor {
	private int number;
	
	public Floor(int number) {
		this.number = number;
	}
	
	public int getNumber() {
		return this.number;
	}
}
